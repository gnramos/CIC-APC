# Em Python, não há o operador de decremento.


print('Decremento:')

i = 2
print('(   i = 2    ) O valor armazenado em i é:', i)

# O decremento é feito com as operações de subtração e atribuição:
i = i - 1
print('(   i = i - 1) O valor armazenado em i é:', i)

# Ou a versão curta da mesma instrução:
i -= 1
print('(  i -= 1    ) O valor armazenado em i é:', i)
