# Exemplos de laços de repetição com FOR.


palavra = 'abcdefghijklmnopqrstuvwxyz'

print('A \'palavra\' é: [%s].' % palavra)

print('Os caracteres de \'palavra\' são:')
for c in palavra:
    print('[%c]' % c)
