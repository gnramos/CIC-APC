# Exemplos de utilização dos condicionais com if.


if 0 == 0:
    print('0 de fato é igual a 0.')
    # a indentação é obrigatória

if 0 == 1:
    print('0 não é igual a 1, espero que esta mensagem nunca seja escrita...')

if 0 < 1:
    print('0 é menor que 1.')

if 0 <= 1:
    print('E 0 também é menor ou igual a 1.')

if 0 > 1:
    print('Mas 0 não é maior que 1.')

if 0 >= 1:
    print('Tampouco é maior ou igual a 1.')
